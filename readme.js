// README
// ARCADE GAME

// START
// To play this game, open the index.html file in your web browser.

// RULES
// The rules fo the game are:
// 1. Don't geht caught by the bugs!
// 2. To suceed reach the water on the top!
// 3. Try to collect as many gems as possible!

// HOW-TO
// Play by using the arrows on your keyboard: Up, Down, Left and Right
// The score is calcuated by your gameplay: Points are added for every
// gem you collect and if you reach the goal. If you colide with an enemy,
// points are substracted.

// PLAY AGAIN
// If you get caught by the bugs or if you reach the water,
// you will be reseted to the starting position and you can try again.

// Have fun!